public class CharArithDemo {

	public static void main(String[] args) {
		char ch;

		ch = 'X';
		System.out.println("ch contains " + ch);

		ch++; // Increment a variable ch
		System.out.println("теперь ch содержит " + ch);

		ch = 90; // Assign variable ch to value Z
		System.out.println("теперь ch содержит " + ch);

	}

}
